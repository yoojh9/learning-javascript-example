let n = 22  		// 32 비트 바이너리: 00000000000000000000000000010110
console.log(n>>1) 	//	00000000000000000000000000001011
console.log(n>>>1)	// 	00000000000000000000000000001011
console.log(n=~n)	// 1의보수: 	11111111111111111111111111101001
console.log(++n)	// 2의보수: 	11111111111111111111111111101010
console.log(n>>1)	//	11111111111111111111111111110101
console.log(n>>>1)	//	01111111111111111111111111110101